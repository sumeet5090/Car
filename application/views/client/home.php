			<h2>Types Of Car Insurance</h2>
		<br>
		<p>Not every four-wheeler insurance policy offers the same coverage. It depends on the type of car insurance policy you buy. There are two main kinds of car insurance policies in India:</p>
		<br>
		<h4>Third Party</h4>
		<p>A Third-party Policy is the most basic type of car insurance policy. It covers, the injuries caused to another person and his/her damaged property. This kind of car insurance policy has the lowest premium. Buying a third-party car insurance policy is mandatory by Indian law.</p>
		<h4>Comprehensive Car Insurance</h4>
		<p>This policy covers all kinds of damages — to your car, another person’s property, and the insured car’s owner-driver. Cause of damages can include Collision, Theft, Fire, Sabotage, Natural occurrences or Man-made calamities.</p>
		<strong>Since Comprehensive Car Insurance offers greater coverage than a Third-party Insurance, it costs more.</strong>
